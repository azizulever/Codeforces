#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, ans;
    cin >> n;
    int N[n];
    while(n--){
        for (int i = 0; i < n; i++){
            cin >> N[i];

            for (int j = 1; j <= n; j++){
                ans = N[0];
                N[1] == ans;
            }
        }

    }

    return 0;
}